    fun statusFor(ageMonths: Int, gender: String, bmi: Double): String? {
        if (ageMonths !in 0..60 || bmi <= 0.0) return null

        val normalizedGender = gender.trim().lowercase()
        val (underweightMax, overweightMin, obeseMin) = when (normalizedGender) {
            "male" -> Triple(boysUnderweightMax, boysOverweightMin, boysObeseMin)
            "female" -> Triple(girlsUnderweightMax, girlsOverweightMin, girlsObeseMin)
            else -> return null
        }

        return when {
            bmi < underweightMax[ageMonths] -> "Underweight"
            bmi > obeseMin[ageMonths] -> "Obese"
            bmi > overweightMin[ageMonths] -> "Overweight"
            else -> "Normal"
        }
    }
